package com.example.buttontextfield;

import javafx.geometry.Bounds;

public class CoordsOnMousePressed {
    private double x;
    private double y;

    public CoordsOnMousePressed() {
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }

}
